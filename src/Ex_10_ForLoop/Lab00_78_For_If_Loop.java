package Ex_10_ForLoop;

public class Lab00_78_For_If_Loop {

    public static void main(String[] args) {
//Its runs for 18 times O/P - 0 -17
        for (int i = 0; i < 18; i++) {
            if (i > 15) {
                System.out.println("Receives gifts");
            } else {
                System.out.println("Receives hugs & toys");

            }

        }

    }
}

