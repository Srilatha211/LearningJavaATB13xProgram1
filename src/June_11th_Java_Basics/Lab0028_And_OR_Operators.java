package June_11th_Java_Basics;

public class Lab0028_And_OR_Operators {

    public static void main(String[] args) {
        System.out.println(true&&true);
        System.out.println(true&&false);
        System.out.println(true&&false);
        System.out.println(false&&false);


        System.out.println(true||true);
        System.out.println(true||false);
        System.out.println(true||false);
        System.out.println(false||false);

    }

}
