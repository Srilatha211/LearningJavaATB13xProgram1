package June_17_ForLoop;

public class Lab00_80_forloop_Float_Double {

    // In for loop float and long can be used but their are not recommended
    public static void main(String[] args) {

        for (float i = 0.0f; i > 10.6; i++) {

            System.out.println(i);

        }
        for (long i = 1l; i > 18; i++) {

            System.out.println(i);
        }
    }
}